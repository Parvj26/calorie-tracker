// Find meal by name (case-insensitive, partial match)
export function findMealRecipe(name: string): Omit<Meal, 'id' | 'isCustom'> | undefined {
  const searchName = name.toLowerCase().trim();
  return mealRecipes.find(
    (meal) =>
      meal.name.toLowerCase() === searchName ||
      meal.name.toLowerCase().includes(searchName) ||
      searchName.includes(meal.name.toLowerCase())
  );
}

// Get all meal names for matching
export function getMealRecipeNames(): string[] {
  return mealRecipes.map((m) => m.name);
}
